// وظائف استرجاع البيانات

export function getAllPosts(): Post[] {
  return posts.sort((a, b) => new Date(b.date).getTime() - new Date(a.date).getTime())
}

export function getPostBySlug(slug: string): Post | undefined {
  return posts.find((post) => post.slug === slug)
}

export function getCategoryPosts(categorySlug: string): Post[] {
  return posts
    .filter((post) => post.category === categorySlug)
    .sort((a, b) => new Date(b.date).getTime() - new Date(a.date).getTime())
}

export function getRelatedPosts(slug: string, limit = 2): Post[] {
  const currentPost = getPostBySlug(slug)
  if (!currentPost) return []

  return posts
    .filter((post) => post.slug !== slug && post.category === currentPost.category)
    .sort((a, b) => new Date(b.date).getTime() - new Date(a.date).getTime())
    .slice(0, limit)
}

export function getCategoryInfo(slug: string): Category | undefined {
  return categories.find((category) => category.slug === slug)
}

export function searchPosts(query: string): Post[] {
  const lowerCaseQuery = query.toLowerCase()

  return posts
    .filter(
      (post) =>
        post.title.toLowerCase().includes(lowerCaseQuery) ||
        post.excerpt.toLowerCase().includes(lowerCaseQuery) ||
        post.content.toLowerCase().includes(lowerCaseQuery) ||
        post.tags.some((tag) => tag.toLowerCase().includes(lowerCaseQuery)),
    )
    .sort((a, b) => new Date(b.date).getTime() - new Date(a.date).getTime())
}
